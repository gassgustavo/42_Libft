#include "libft.h"

char	*ft_strjoin(char const *str1, char const *str2)
{
	char	*n_str;
	size_t	str1_len;
	size_t	str2_len;

	str1_len = ft_strlen(str1);
	str2_len = ft_strlen(str2);
	n_str = malloc(str1_len + str2_len + 1);
	if (!n_str)
		return (0);

	return (ft_strlcat(str1,str2, n_str));
}
