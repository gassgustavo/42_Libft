#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	len;

	if (str[0] == '\0')
		return (0);
	len = ft_strlen(str);
	while (str[len] != (unsigned char)c && len >= 0)
		len--;
	if (str[len] == (unsigned char)c)
		return ((char *)&str[len]);
	else
		return (0);
}
